const expressValidator = require('express-validator');
const { body, validationResult } = expressValidator;

// Validation result handler middleware
export const handleValidationErrors = (req: any, res: any, next: any) => {
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    return res.status(400).json({
      message: 'Validation errors',
      errors: errors.array()
    });
  }
  next();
};

export const registerValidation = [
  body('email')
    .isEmail()
    .normalizeEmail()
    .withMessage('Please provide a valid email'),
  body('password')
    .isLength({ min: 6 })
    .withMessage('Password must be at least 6 characters long'),
  body('name')
    .trim()
    .isLength({ min: 2, max: 50 })
    .withMessage('Name must be between 2 and 50 characters')
];

export const loginValidation = [
  body('email')
    .isEmail()
    .normalizeEmail()
    .withMessage('Please provide a valid email'),
  body('password')
    .notEmpty()
    .withMessage('Password is required')
];

export const sessionValidation = [
  body('title')
    .trim()
    .isLength({ min: 1, max: 100 })
    .withMessage('Title is required and must be less than 100 characters'),
  body('tags')
    .optional()
    .isArray()
    .withMessage('Tags must be an array'),
  body('tags.*')
    .optional()
    .trim()
    .isLength({ max: 30 })
    .withMessage('Each tag must be less than 30 characters'),
  body('jsonUrl')
    .optional()
    .custom((value: string) => {
      if (!value) return true;
      // Allow either full URLs or JSON filenames
      const urlRegex = /^https?:\/\/.+/;
      const filenameRegex = /^[\w\-_.]+\.(json|JSON)$/;
      return urlRegex.test(value) || filenameRegex.test(value);
    })
    .withMessage('JSON URL must be a valid URL or JSON filename')
];
